                <p class="tilteblog fade-in">Lacto-Végétarisme :</p>
                <p class="textblog fade-in">
                    Le régime lacto-végétarien est l'un des plus répandus. Il exclut la viande, le poisson, et les œufs, mais autorise les produits laitiers comme le lait et le fromage. Ce régime est apprécié pour sa capacité à fournir des nutriments essentiels tout en limitant la consommation de produits animaux.
                </p>
                
                <p class="tilteblog fade-in">Ovo-Végétarisme :</p>
                <p class="textblog fade-in">
                    Le régime ovo-végétarien évite la viande, le poisson et les produits laitiers, mais inclut les œufs. Cela constitue une source importante de protéines pour ceux qui ne consomment pas de produits laitiers.
                </p>
                
                <p class="tilteblog fade-in">Lacto-Ovo-Végétarisme :</p>
                <p class="textblog fade-in">
                    Le régime lacto-ovo-végétarien combine la consommation de produits laitiers et d'œufs tout en excluant la viande et le poisson. Ce type de régime est souvent recommandé car il équilibre l'apport en protéines tout en minimisant les risques associés à une consommation excessive de viande.
                </p>
                
                <p class="tilteblog fade-in">Végétalisme :</p>
                <p class="textblog fade-in">
                    Le régime végétalien exclut tous les produits d'origine animale, y compris les produits laitiers, les œufs et le miel. Ce régime repose principalement sur des fruits, légumes, céréales complètes, noix et graines, souvent adopté pour des raisons éthiques, écologiques ou de santé.
                </p>

