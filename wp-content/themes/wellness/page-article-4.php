                </p>

                <p class="textblog fade-in">Fibres : </p>
                <p class="textblog fade-in">
                Les fibres sont essentielles pour une digestion saine et aident à maintenir un poids corporel stable en favorisant une sensation de satiété. Les légumineuses, comme les lentilles, les pois chiches et les haricots, sont particulièrement riches en fibres. Les fibres aident également à réguler la glycémie et à réduire le risque de maladies cardiovasculaires.

                </p>


